using Xunit;
using Xunit.Generators;

public class RegisterResultWriterAttributeGeneratorTests : RunnerCommonGeneratorTest<RegisterResultWriterAttributeGenerator>
{
	[Fact]
	public void TypeMissingInterface()
	{
		var source = /* lang=c#-test */ """
			using Xunit.Runner.Common;

			[assembly: RegisterResultWriter("foo", typeof(object))]
			""";

		var result = Generate(source);

		Assert.Empty(result.GeneratedSources);
	}

	[Fact]
	public void SingleRegistration()
	{
		var source = /* lang=c#-test */ """
			using Xunit.Runner.Common;

			[assembly: RegisterResultWriter("foo", typeof(XmlV2ResultWriter))]
			""";

		var result = GenerateSingleSource(source);

		Assert.Equal(/* lang=c#-test */ """
			// <auto-generated> This file has been auto generated by xunit.v3.runner.common.aot.generators. </auto-generated>

			#nullable enable

			[assembly: global::Xunit.AutoGenerated._47DEQpj8H.RegisterResultWriterAttribute٠GeiTSPKp1]

			namespace Xunit.AutoGenerated._47DEQpj8H {
				[global::System.Diagnostics.CodeAnalysis.ExcludeFromCodeCoverage]
				internal class RegisterResultWriterAttribute٠GeiTSPKp1 : global::Xunit.Runner.Common.RunnerInitializationAttribute {
					public override async global::System.Threading.Tasks.ValueTask InitializeAsync() {
						{
							var writer = new Xunit.Runner.Common.XmlV2ResultWriter();
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterConsoleResultWriter("foo", writer);
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterMicrosoftTestingPlatformResultWriter("foo", writer);
						}
					}
				}
			}
			""", result);
	}

	[Fact]
	public void DoubleRegistration()
	{
		var source = /* lang=c#-test */ """
			using Xunit.Runner.Common;

			[assembly: RegisterResultWriter("foo", typeof(XmlV2ResultWriter))]
			[assembly: RegisterResultWriter("bar", typeof(XmlV2ResultWriter))]
			""";

		var result = GenerateSingleSource(source);

		Assert.Equal(/* lang=c#-test */ """
			// <auto-generated> This file has been auto generated by xunit.v3.runner.common.aot.generators. </auto-generated>

			#nullable enable

			[assembly: global::Xunit.AutoGenerated._47DEQpj8H.RegisterResultWriterAttribute٠GeiTSPKp1]

			namespace Xunit.AutoGenerated._47DEQpj8H {
				[global::System.Diagnostics.CodeAnalysis.ExcludeFromCodeCoverage]
				internal class RegisterResultWriterAttribute٠GeiTSPKp1 : global::Xunit.Runner.Common.RunnerInitializationAttribute {
					public override async global::System.Threading.Tasks.ValueTask InitializeAsync() {
						{
							var writer = new Xunit.Runner.Common.XmlV2ResultWriter();
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterConsoleResultWriter("foo", writer);
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterMicrosoftTestingPlatformResultWriter("foo", writer);
						}
						{
							var writer = new Xunit.Runner.Common.XmlV2ResultWriter();
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterConsoleResultWriter("bar", writer);
							global::Xunit.Runner.Common.RegisteredRunnerConfig.RegisterMicrosoftTestingPlatformResultWriter("bar", writer);
						}
					}
				}
			}
			""", result);
	}
}
